// ── Helper: get events by status ─────────────────────────
export function getEventsByStatus(status) {
  return BHOPAL_EVENTS.filter(e => e.status === status)
}

// ── Helper: get events by category ───────────────────────
export function getEventsByCategory(category) {
  if (category === 'all') return BHOPAL_EVENTS
  return BHOPAL_EVENTS.filter(e => e.category === category)
}

// ── Helper: get events by ward ────────────────────────────
export function getEventsByWard(ward) {
  return BHOPAL_EVENTS.filter(e => e.ward.includes(ward))
}

// ── Helper: search events ─────────────────────────────────
export function searchEvents(query) {
  const q = query.toLowerCase()
  return BHOPAL_EVENTS.filter(e =>
    e.name.toLowerCase().includes(q) ||
    e.description.toLowerCase().includes(q) ||
    e.venue.toLowerCase().includes(q) ||
    e.tags.some(t => t.includes(q))
  )
}
